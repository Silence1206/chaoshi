package com.accp.shop.pojo;

public class StoragePojo {
private String cargo_id;
private String cargo_name;
private String cargo_type;
private int storage_num;
public String getCargo_id() {
	return cargo_id;
}
public void setCargo_id(String cargo_id) {
	this.cargo_id = cargo_id;
}
public int getStorage_num() {
	return storage_num;
}
public void setStorage_num(int storage_num) {
	this.storage_num = storage_num;
}
public String getCargo_name() {
	return cargo_name;
}
public void setCargo_name(String cargo_name) {
	this.cargo_name = cargo_name;
}
public String getCargo_type() {
	return cargo_type;
}
public void setCargo_type(String cargo_type) {
	this.cargo_type = cargo_type;
}

}
